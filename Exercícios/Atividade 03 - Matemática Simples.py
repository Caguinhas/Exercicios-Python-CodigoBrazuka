print("Calculadora de soma, subtração, multiplicação e divisão")

a = int(input("Digite um número: "))
b = int(input("Digite outro número: "))

soma = a + b
sub = a - b
multi = a * b
div = a / b

print(a," + ", b," = ", soma, "\n",
      a, " - ", b," = ", sub, "\n",
      a, " * ", b," = ", multi, "\n",
      a, " / ", b, " = ", div, "\n")